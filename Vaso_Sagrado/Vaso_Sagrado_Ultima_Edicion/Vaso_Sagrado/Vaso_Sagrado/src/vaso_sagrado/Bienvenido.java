    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
        try
        {
            Connection cn = Conexion.conectar("VasoSagrado", "sa", "sasa");
            PreparedStatement ps = cn.prepareStatement("select * from Usuarios where Nombre=? and Pass=?");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jPasswordField1.getText());
            ResultSet rs = ps.executeQuery();
            if(rs.next())
            {
                int nivel = rs.getInt("nivel");
                if(nivel==1)
                {
                    new Menu_Administrador().setVisible(true);
                    dispose();
                }
                else if(nivel == 2)
                {
                    new Menu_Usuario().setVisible(true);
                    dispose();
                }
                else if(nivel == 3)
                {
                    new Menu_Trabajador().setVisible(true);
                    dispose();
                }
            }

        } catch (SQLException ex) { JOptionPane.showMessageDialog(this, "Usuario y/o Password incorrecto"); }
        
    }//GEN-LAST:event_jButton1ActionPerformed

