    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
         try
        {
            Connection cn = Conexion.conectar("VasoSagrado", "sa", "sasa");
            PreparedStatement ps=cn.prepareStatement("select * from Trabajo_Realizar where idEmpleado=?");
            ps.setString(1, jTextField1.getText());
            ResultSet resultado=ps.executeQuery();
            if(resultado.next())
            {
                String cadena=resultado.getString("Piezas")+"\n\n   *Comentarios:\n";
                cadena+=resultado.getString("Comentarios")+"\n";
                jTextArea1.setText(cadena);
            }
        else
        {
            JOptionPane.showMessageDialog(null,"No existe esa matricula");
            jTextArea1.setText("");
        }
        } catch (Exception e) { System.out.println("Error "+e);}
    }//GEN-LAST:event_jButton1MouseClicked


